class Libro {
  constructor(
    id,
    titulo,
    autor,
    sinopsis,
    precio,
    oferta,
    precioOferta,
    portada,
    stock
  ) {
    this.id = id;
    this.titulo = titulo;
    this.autor = autor;
    this.sinopsis = sinopsis;
    this.precio = precio;
    this.oferta = oferta;
    this.precioOferta = precioOferta;
    this.portada = portada;
    this.stock = stock
  }
}
